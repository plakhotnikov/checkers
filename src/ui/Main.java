/*
 * ПРАВИЛА
 * >>>>>>>>>>>>>
 * 1. Шашки могут перемещаться только по диагонали по тёмным клеткам.
 *
 * 2. Обычные шашки могут двигаться только вперёд по диагонали
 *    (чёрные — вниз, белые — вверх).
 *
 * 3. Шашка становится дамкой, когда достигает противоположного края доски
 *    и больше не может двигаться вперёд.
 *
 * 4. После превращения в дамку ход игрока заканчивается.
 *
 * 5. После перемещения шашки или дамки на одну клетку по диагонали
 *    ход игрока заканчивается.
 *
 * 6. Если можно выполнить взятие (перепрыгнуть через шашку соперника),
 *    взятие обязательно.
 *
 * 7. Если после взятия та же шашка может взять снова, она должна продолжить,
 *    иначе ход заканчивается.
 *
 * 8. Игра завершается, если у игрока не осталось шашек или он не может
 *    сделать ход в свой ход.
 *
 * 9. Игрок с чёрными шашками ходит первым.
 */

package ui;

import javax.swing.UIManager;

/**
 * Класс {@code Main} является точкой входа в приложение.
 * Он устанавливает системный стиль оформления и создаёт главное окно
 * для игры в шашки {@link CheckersWindow}.
 */
public class Main {

	public static void main(String[] args) {

		// Устанавливаем системный look-and-feel
		try {
			UIManager.setLookAndFeel(
					UIManager.getSystemLookAndFeelClassName());
		} catch (Exception e) {
			e.printStackTrace();
		}

		// Создаём и отображаем окно с игрой в шашки
		CheckersWindow window = new CheckersWindow();
		window.setDefaultCloseOperation(CheckersWindow.EXIT_ON_CLOSE);
		window.setVisible(true);
	}
}